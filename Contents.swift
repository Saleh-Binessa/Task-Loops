import UIKit

// Task 1: Iterating Over an Array
let favoriteFruits = ["Apple", "Banana", "Cherry"]
for fruits in favoriteFruits {
    print(fruits)
}

// Task 2: Nested Loops with Strings
for i in 1...5 {
    for j in 1...5 {
        let result = i * j
        print("\(i) * \(j) = \(result)")
    }
}
